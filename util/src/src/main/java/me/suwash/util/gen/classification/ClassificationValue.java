package me.suwash.util.gen.classification;

public class ClassificationValue {
    private String id;
    private String name;
    private String storeValue;

    /**
     * @return id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id
     *            セットする id
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name
     *            セットする name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return storeValue
     */
    public String getStoreValue() {
        return storeValue;
    }

    /**
     * @param storeValue
     *            セットする storeValue
     */
    public void setStoreValue(String storeValue) {
        this.storeValue = storeValue;
    }}
